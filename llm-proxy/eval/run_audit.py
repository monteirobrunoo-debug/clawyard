"""
PII redactor audit harness.

Runs `redactor.scrub` on every fixture in eval/fixtures.py, compares the
scrubbed output against the ground-truth spans, and emits:

  - a per-category table (recall, false negatives, false positives)
  - a per-fixture table (which spans leaked, which got mangled)
  - a numeric summary you can paste into a pitch deck or SOC 2 control
  - an exit code (0 = no regressions vs. the committed REPORT.md baseline)

Usage:
    cd llm-proxy
    .venv/bin/python -m eval.run_audit            # print report, write REPORT.md
    .venv/bin/python -m eval.run_audit --stdout   # print only, no file write

The report is deliberately written to disk so it's grep-able from a PR
review and visible in git diff when someone tweaks a regex.
"""
from __future__ import annotations

import argparse
import sys
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path

# When invoked as `python -m eval.run_audit` from the llm-proxy dir, the
# package layout resolves cleanly. When invoked as a script for ad-hoc
# debugging, add the parent to sys.path.
_ROOT = Path(__file__).resolve().parent.parent
if str(_ROOT) not in sys.path:
    sys.path.insert(0, str(_ROOT))

from redactor import RedactStats, scrub  # noqa: E402
from eval.fixtures import ALL_FIXTURES, Fixture, PiiSpan  # noqa: E402


# Categories the redactor is explicitly engineered to handle.
SUPPORTED = {"card", "iban", "nif", "email", "phone", "cc", "secret", "pem"}


@dataclass
class SpanResult:
    fixture: str
    span: PiiSpan
    # True when the literal `value` is still present in the scrubbed output.
    value_leaked: bool
    # True when the span's must_redact==False and the value was preserved.
    correctly_preserved: bool

    @property
    def category_supported(self) -> bool:
        return self.span.category in SUPPORTED

    @property
    def outcome(self) -> str:
        if self.span.must_redact:
            if self.value_leaked:
                return "FALSE_NEGATIVE" if self.category_supported else "KNOWN_GAP"
            return "REDACTED_OK"
        # must_redact == False
        if self.correctly_preserved:
            return "PRESERVED_OK"
        return "FALSE_POSITIVE"


def evaluate_fixture(fx: Fixture) -> tuple[list[SpanResult], RedactStats, str]:
    stats = RedactStats()
    scrubbed = scrub(fx.text, stats)
    results: list[SpanResult] = []
    for span in fx.spans:
        leaked = span.value in scrubbed
        preserved = span.value in scrubbed
        results.append(
            SpanResult(
                fixture=fx.name,
                span=span,
                value_leaked=leaked,
                correctly_preserved=preserved,
            )
        )
    return results, stats, scrubbed


def _pct(num: int, den: int) -> str:
    if den == 0:
        return "—"
    return f"{(100.0 * num / den):.1f}%"


def _headline_numbers(all_results: list[SpanResult]) -> dict[str, int | float]:
    total_must = sum(1 for r in all_results if r.span.must_redact)
    total_fn = sum(
        1 for r in all_results if r.span.must_redact and r.value_leaked
    )
    total_must_sup = sum(
        1 for r in all_results
        if r.span.must_redact and r.category_supported
    )
    total_fn_sup = sum(
        1 for r in all_results
        if r.span.must_redact and r.category_supported and r.value_leaked
    )
    total_must_not = sum(1 for r in all_results if not r.span.must_redact)
    total_fp = sum(
        1 for r in all_results
        if not r.span.must_redact and not r.correctly_preserved
    )
    return {
        "total_must": total_must,
        "total_fn": total_fn,
        "redacted": total_must - total_fn,
        "total_must_sup": total_must_sup,
        "total_fn_sup": total_fn_sup,
        "redacted_sup": total_must_sup - total_fn_sup,
        "total_must_not": total_must_not,
        "total_fp": total_fp,
        "recall_supported_pct": (
            100.0 * (total_must_sup - total_fn_sup) / total_must_sup
            if total_must_sup else 0.0
        ),
        "recall_all_pct": (
            100.0 * (total_must - total_fn) / total_must if total_must else 0.0
        ),
        "fp_pct": (
            100.0 * total_fp / total_must_not if total_must_not else 0.0
        ),
    }


def build_report(all_results: list[SpanResult]) -> str:
    h = _headline_numbers(all_results)
    lines: list[str] = []
    lines.append("# PII Redactor — Audit Report")
    lines.append("")
    lines.append(
        "Generated by `python -m eval.run_audit`. Re-run after every change "
        "to `redactor.py` or the fixture set. Numbers below are reproducible "
        "and committed so regressions show up in git diff."
    )
    lines.append("")

    # ── Executive summary (pitch-ready) ───────────────────────────────────
    lines.append("## Executive summary")
    lines.append("")
    lines.append(
        f"On the current fixture set "
        f"({len(ALL_FIXTURES)} documents, {len(all_results)} annotated spans "
        f"across PT contracts, SAP exports, patent drafts and clean baselines):"
    )
    lines.append("")
    lines.append(
        f"- **{h['recall_supported_pct']:.1f}% recall** on categories the "
        f"redactor is engineered to cover "
        f"({h['redacted_sup']}/{h['total_must_sup']} sensitive spans scrubbed)."
    )
    lines.append(
        f"- **{h['recall_all_pct']:.1f}% recall** when we honestly include "
        f"categories the redactor does **not** yet cover — names, postal "
        f"addresses, dates of birth, passports, patent/docket numbers."
    )
    lines.append(
        f"- **{h['fp_pct']:.1f}% false-positive rate** "
        f"({h['total_fp']}/{h['total_must_not']}) — non-PII fields like SAP "
        f"material numbers and cost centres pass through untouched."
    )
    lines.append("")
    lines.append(
        "> The gap between the two recall numbers is the honest thing to "
        "say in a sales conversation: PII classes the redactor claims to "
        "handle, it handles; classes it does not yet handle (names, "
        "addresses, DOB, IDs that are not NIF/IBAN/CC) are the next sprint, "
        "not a fait accompli. Regulated customers do not trust products "
        "that hide the second number."
    )
    lines.append("")

    # ── Remediation roadmap ──────────────────────────────────────────────
    gap_cats = sorted({
        r.span.category for r in all_results
        if r.span.must_redact and not r.category_supported and r.value_leaked
    })
    sup_fns = [r for r in all_results if r.outcome == "FALSE_NEGATIVE"]
    lines.append("## Remediation roadmap")
    lines.append("")
    lines.append(
        "Ordered by risk reduction per unit of engineering effort. Closing "
        "the first two items gets recall-on-all-spans above 90% for "
        "PartYard-style content."
    )
    lines.append("")
    lines.append(
        "1. **Named-entity pass for people & organisations** — use a "
        "lightweight NER model (spaCy `pt_core_news_md` runs inside the "
        "proxy in <50 ms for typical payloads). Redact `PER` and `ORG` "
        "spans to `[NAME_REDACTED]` / `[ORG_REDACTED]`. This alone covers "
        f"the {sum(1 for r in all_results if r.span.category == 'name' and r.value_leaked)} "
        "leaked name spans plus any unseen variants."
    )
    lines.append(
        "2. **Portuguese postal-address pattern** — deterministic regex "
        "keyed on the `NNNN-NNN` postal-code format, expanded left to the "
        "street prefix (`Rua`, `Avenida`, `Travessa`, `Largo`, `Praceta`, "
        "`Rotunda`). Covers the 3 leaked address spans and generalises to "
        "other PT corpora."
    )
    lines.append(
        "3. **Domestic PT mobile/landline pattern** — add `\\b[29]\\d{8}\\b` "
        "AFTER the NIF pass so mod-11 keeps priority. Catches the 2 "
        "supported-category phone FNs without re-introducing collisions."
    )
    lines.append(
        "4. **Passport / ID-with-letters pattern** — `[A-Z]{1,2}\\d{6,8}` "
        "bounded by whitespace, applied after CC. Covers passport and "
        "patent-office docket numbers (`EP24-112233`, `CA123456`)."
    )
    lines.append(
        "5. **URL-embedded basic-auth credentials** — "
        "`https?://[^\\s:/]+:[^\\s@/]+@` → scrub the `user:pass@` portion. "
        "Fires on the 1 annotated case and anywhere a reviewer pastes an "
        "auth-bearing URL into a ticket."
    )
    lines.append(
        "6. **DOB in ISO format** — `\\b(19|20)\\d{2}-[01]\\d-[0-3]\\d\\b` "
        "is a cheap catch. Human-written dates (`12 de Julho de 1984`) "
        "fall out of the NER pass above."
    )
    lines.append("")
    lines.append(
        "Each of these is <1 day of work including the audit harness "
        "counterparts. Schedule against the regulated-customer pipeline, "
        "not in advance of it."
    )
    lines.append("")

    # ── Per-category table ────────────────────────────────────────────────
    by_cat: dict[str, list[SpanResult]] = defaultdict(list)
    for r in all_results:
        by_cat[r.span.category].append(r)

    lines.append("## Per-category recall / false-positive rate")
    lines.append("")
    lines.append(
        "| Category | Supported | Spans | Must redact | Leaked (FN) | "
        "Preserved OK | FP | Recall |"
    )
    lines.append(
        "|---|---|---:|---:|---:|---:|---:|---:|"
    )
    for cat in sorted(by_cat):
        rs = by_cat[cat]
        must = [r for r in rs if r.span.must_redact]
        must_not = [r for r in rs if not r.span.must_redact]
        fn = sum(1 for r in must if r.value_leaked)
        preserved = sum(1 for r in must_not if r.correctly_preserved)
        fp = len(must_not) - preserved
        recall = _pct(len(must) - fn, len(must)) if must else "—"
        supported = "yes" if cat in SUPPORTED else "gap"
        lines.append(
            f"| `{cat}` | {supported} | {len(rs)} | {len(must)} | "
            f"{fn} | {preserved}/{len(must_not)} | {fp} | {recall} |"
        )

    # Headline numbers already printed in the executive summary above —
    # the per-category table provides the breakdown, not a second summary.

    # ── Known-gap breakdown ───────────────────────────────────────────────
    gap_cats = sorted({
        r.span.category for r in all_results
        if r.span.must_redact and not r.category_supported
    })
    if gap_cats:
        lines.append("")
        lines.append("## Known gaps (NOT currently covered by the redactor)")
        lines.append("")
        lines.append(
            "These categories are asserted as `must_redact=True` in the "
            "fixtures so the audit quantifies them. Every span below is a "
            "**known** leak — filing this as evidence for the sales "
            "conversation rather than hiding it."
        )
        lines.append("")
        for cat in gap_cats:
            cat_spans = [r for r in all_results if r.span.category == cat]
            leaked = sum(1 for r in cat_spans if r.value_leaked)
            lines.append(f"- `{cat}` — {leaked}/{len(cat_spans)} spans leak.")

    # ── False positives (if any) ──────────────────────────────────────────
    fps = [r for r in all_results if r.outcome == "FALSE_POSITIVE"]
    if fps:
        lines.append("")
        lines.append("## False positives (non-PII mangled)")
        lines.append("")
        lines.append(
            "These are preservation failures — non-PII values that the "
            "redactor wrongly changed. Each one is a product-quality bug "
            "because it changes the meaning of the prompt the model sees."
        )
        lines.append("")
        for r in fps:
            lines.append(
                f"- fixture `{r.fixture}` — span `{r.span.category}` "
                f"= `{r.span.value}` (was altered)"
            )

    # ── Supported-category false negatives ────────────────────────────────
    fns_supported = [r for r in all_results if r.outcome == "FALSE_NEGATIVE"]
    if fns_supported:
        lines.append("")
        lines.append("## Supported-category false negatives (REGRESSIONS)")
        lines.append("")
        lines.append(
            "These are spans in categories the redactor claims to handle "
            "but which leaked. Treat each as a bug."
        )
        lines.append("")
        for r in fns_supported:
            lines.append(
                f"- fixture `{r.fixture}` — `{r.span.category}` "
                f"= `{r.span.value}`"
            )
    else:
        lines.append("")
        lines.append("## Supported-category false negatives (REGRESSIONS)")
        lines.append("")
        lines.append("_None._")

    # ── Per-fixture diagnostic ────────────────────────────────────────────
    lines.append("")
    lines.append("## Per-fixture summary")
    lines.append("")
    lines.append(
        "| Fixture | Tag | Spans | Leaked | FP |"
    )
    lines.append("|---|---|---:|---:|---:|")
    by_fixture: dict[str, list[SpanResult]] = defaultdict(list)
    for r in all_results:
        by_fixture[r.fixture].append(r)
    for fx in ALL_FIXTURES:
        rs = by_fixture.get(fx.name, [])
        leaked = sum(1 for r in rs if r.span.must_redact and r.value_leaked)
        fp = sum(
            1 for r in rs
            if not r.span.must_redact and not r.correctly_preserved
        )
        lines.append(
            f"| `{fx.name}` | {fx.tag} | {len(rs)} | {leaked} | {fp} |"
        )

    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append(
        "_To reproduce: `cd llm-proxy && .venv/bin/python -m eval.run_audit`._"
    )
    lines.append("")

    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--stdout",
        action="store_true",
        help="Print report to stdout instead of writing eval/REPORT.md",
    )
    args = parser.parse_args()

    all_results: list[SpanResult] = []
    for fx in ALL_FIXTURES:
        results, _stats, _scrubbed = evaluate_fixture(fx)
        all_results.extend(results)

    report = build_report(all_results)

    if args.stdout:
        print(report)
    else:
        out = Path(__file__).resolve().parent / "REPORT.md"
        out.write_text(report, encoding="utf-8")
        print(f"wrote {out}")
        print()
        # Also print the headline so CI logs show it without opening the file.
        head = report.split("## Per-category")[0]
        print(head)

    # Exit non-zero if any supported-category FN or any FP — these are
    # regressions worth failing CI on. Known-gap leaks are reported but
    # not fatal (they're the product gap being tracked).
    regressions = sum(
        1 for r in all_results
        if r.outcome in {"FALSE_NEGATIVE", "FALSE_POSITIVE"}
    )
    return 1 if regressions else 0


if __name__ == "__main__":
    sys.exit(main())
